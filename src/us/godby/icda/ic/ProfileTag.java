package us.godby.icda.ic;

public class ProfileTag {

	private String term = "";
	private String frequency = "";
	private String intensityBin = "";
	private String visibilityBin = "";
	private String type = "";
	
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getFrequency() {
		return frequency;
	}
	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}
	public String getIntensityBin() {
		return intensityBin;
	}
	public void setIntensityBin(String intensityBin) {
		this.intensityBin = intensityBin;
	}
	public String getVisibilityBin() {
		return visibilityBin;
	}
	public void setVisibilityBin(String visibilityBin) {
		this.visibilityBin = visibilityBin;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

}
